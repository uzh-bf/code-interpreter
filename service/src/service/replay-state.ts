// ---------------------------------------------------------------------------
// Constants
// ---------------------------------------------------------------------------

export const EXECUTION_STATE_TTL = 600;
export const MAX_REPLAY_CALLS = 200;

/** Cap on the serialized `exec_state:<id>` JSON blob (request inputs +
 * bookkeeping: `userCode`, `tools` schema, input `files`, `emittedCallIds`,
 * counters — does NOT include tool outputs, those live in `tool_history:`).
 * Sized to comfortably absorb realistic LLM-generated code plus a handful of
 * input files; the per-result and aggregate `tool_history` caps below scale
 * proportionally. */
export const MAX_EXECUTION_STATE_BYTES = 10_000_000;

/** Per-execution continuation lock TTL. Must exceed the worst-case sandbox
 * run (`env.JOB_TIMEOUT`) plus bookkeeping overhead, otherwise the lock can
 * expire while the holder is still executing and a second continuation
 * would be able to mutate `tool_history`/`exec_state` concurrently. Floor
 * at 10 minutes so short job timeouts don't produce a tiny lock window. */
export const REPLAY_LOCK_TTL_MS = Math.max(10 * 60 * 1000, env.JOB_TIMEOUT * 2 + 30_000);

/** Per-entry cap for a single serialized tool result (JSON bytes). Keeps the
 * Redis hash and the `_ptc_history.json` injected into the sandbox bounded
 * regardless of how pathological a single tool result is. Scaled
 * proportionally with `MAX_EXECUTION_STATE_BYTES` (ratio 1:2 vs exec_state). */
export const MAX_TOOL_RESULT_BYTES = 5_000_000;

/** Aggregate cap across ALL results persisted for a single execution. Scaled
 * proportionally with `MAX_EXECUTION_STATE_BYTES` (ratio 4:1 vs exec_state)
 * so a long replay flow can accumulate ~8 saturating tool results before
 * being asked to break work into a fresh execution. */
export const MAX_TOOL_HISTORY_TOTAL_BYTES = 40_000_000;

/** Maximum number of keys `scanKeys` will return in a single call. The janitor
 * runs every `STALE_CLEANUP_INTERVAL` and processes whatever this yields; if
 * Redis is genuinely backed up with `exec_state:*` entries beyond this cap,
 * the next tick will pick up the rest. The point is to avoid an unbounded
 * in-memory array on a degenerate Redis instance, NOT to throttle work. */
export const SCAN_KEYS_DEFAULT_LIMIT = 10_000;

/** Replay call_ids are generated by the sandbox as zero-padded 3-digit
 * counters (`call_001`...); we allow up to 6 digits of slack so future
 * cap bumps don't require a regex change, but cap length so a client
 * can't submit pathologically long ids that would bloat Redis hash
 * field names beyond what `MAX_TOOL_RESULT_BYTES` or the aggregate cap
 * would catch (those bound values, not keys). */
export const CALL_ID_RE = /^call_\d{3,6}$/;

