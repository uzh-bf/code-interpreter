/** Lua scripts cached on the client via `defineCommand` so each call site
 * issues `EVALSHA` (with auto `SCRIPT LOAD` fallback) instead of paying the
 * Lua compile + parse cost of `EVAL` on every invocation. Scripts:
 *
 *   - `releaseExecutionLockScript`  CAS-DEL on the lock key (only the holder
 *                                   may release it — guards stale releases).
 *   - `setExecutionResultScript`    EXISTS-then-dual-SET (state + result)
 *                                   so a concurrent `cleanupExecution` can't
 *                                   resurrect an orphan `exec_result` blob.
 *   - `setExecutionErrorScript`     EXISTS-then-SET (state) — same race
 *                                   guard for the error path.
 *
 * Note: `ioredis-mock` implements `defineCommand` and routes through its
 * Lua runner, but its Lua coverage is a subset of real Redis (notably
 * partial `redis.call` arg parsing); the integration suite still runs
 * against a real Redis container so any divergence surfaces there. */
type RedisWithScripts = Redis & {
  releaseExecutionLockScript(lockKey: string, token: string): Promise<number>;
  setExecutionResultScript(
    stateKey: string,
    resultKey: string,
    stateJson: string,
    resultJson: string,
    ttlSeconds: string,
  ): Promise<number>;
  setExecutionErrorScript(
    stateKey: string,
    stateJson: string,
    ttlSeconds: string,
  ): Promise<number>;
};

const SCRIPTS_REGISTERED = Symbol.for('replay-state.scriptsRegistered');

function registerScripts(client: Redis): RedisWithScripts {
  const tagged = client as Redis & { [SCRIPTS_REGISTERED]?: true };
  if (tagged[SCRIPTS_REGISTERED]) return client as RedisWithScripts;
  client.defineCommand('releaseExecutionLockScript', {
    numberOfKeys: 1,
    lua: "if redis.call('get', KEYS[1]) == ARGV[1] then return redis.call('del', KEYS[1]) else return 0 end",
  });
  client.defineCommand('setExecutionResultScript', {
    numberOfKeys: 2,
    lua: `if redis.call('exists', KEYS[1]) == 1 then
  redis.call('set', KEYS[1], ARGV[1], 'EX', ARGV[3])
  redis.call('set', KEYS[2], ARGV[2], 'EX', ARGV[3])
  return 1
else
  return 0
end`,
  });
  client.defineCommand('setExecutionErrorScript', {
    numberOfKeys: 1,
    lua: `if redis.call('exists', KEYS[1]) == 1 then
  redis.call('set', KEYS[1], ARGV[1], 'EX', ARGV[2])
  return 1
else
  return 0
end`,
  });
  tagged[SCRIPTS_REGISTERED] = true;
  return client as RedisWithScripts;
}

/** Module-level Redis handle. Defaults to the shared queue connection so
 * production code paths are unchanged; tests can swap in an `ioredis-mock`
 * instance via `setRedisForTests()` to exercise the pure replay-state logic
 * without spinning up real Redis or the queue subsystem. */
let redis: RedisWithScripts = registerScripts(connection);

/** Replace the Redis handle used by every function in this module. Intended
 * ONLY for unit tests; production code should never call this. The pair
 * `setRedisForTests`/`resetRedisForTests` lets a test suite isolate per-test
 * keyspaces by handing in fresh mock instances. */
export function setRedisForTests(client: Redis): void {
  redis = registerScripts(client);
}

/** Restore the production Redis handle. Useful after a test suite finishes
 * so accidental reuse doesn't leak a closed mock connection. */
export function resetRedisForTests(): void {
  redis = registerScripts(connection);
}

